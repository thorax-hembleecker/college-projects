            <p>Espresso is a concentrated form of coffee produced by forcing hot water under high pressure through finely-ground coffee beans. Originating in Italy, espresso has become one of the most popular coffee-brewing methods worldwide. It is characterized by its small serving size, typically 25–30 ml, and its distinctive layers: a dark body topped with a lighter-colored foam called crema.
            </p>
            <p>Espresso machines use pressure to extract a highly concentrated coffee with a complex flavor profile in a short time, usually 25–30 seconds. The result is a beverage with a higher concentration of suspended and dissolved solids than regular drip coffee, giving espresso its characteristic body and intensity.
            </p>
            <p>Espresso serves as the base for other coffee drinks, including cappuccino, latte, and americano. It can be made with various types of coffee beans and roast levels, allowing for a wide range of flavors and strengths. The quality of an espresso is influenced by factors such as the grind size, water temperature, pressure, and the barista's skill in tamping the coffee grounds.
            </p>
            <p>While espresso contains more caffeine per unit volume than most coffee beverages, its typical serving size results in less caffeine per serving compared to larger drinks such as drip coffee. The cultural significance of espresso extends beyond its consumption, playing a central role in coffee shop culture and the third-wave coffee movement, which emphasizes artisanal production and high-quality beans.
            </p>
            <h2>Etymology and spelling</h2>
                <p>Some English dictionaries translate espresso as 'pressed-out', but the word also conveys the senses of expressly for you and quickly:
                </p>
                <blockquote>The words <em>express</em>, <em>expres</em> and <em>espresso</em> each have several meanings in English, French and Italian. The first meaning is to do with the idea of "expressing" ("pressing out of") or squeezing the flavour from the coffee using the pressure of the steam. The second meaning is to do with speed, as in a train. Finally there is the notion of doing something "expressly" for a person ... The first Bezzera and Pavoni espresso machines in 1906 took 45 seconds to make a cup of coffee, one at a time, expressly for you.
                </blockquote>
                <p>Modern espresso, using hot water under pressure, as pioneered by Gaggia in the 1940s, was originally called crema caffè (lit. 'cream coffee'), as seen on old Gaggia machines, due to the crema. This term is no longer used, although crema caffè and variants (caffè crema, café crema) still appear in branding.
                </p>
                <p>The spelling expresso is mostly considered incorrect, though some sources call it a less common variant. It is common in French and Portuguese. Italy uses the term espresso, substituting s for most x letters in Latin-root words, with the term deriving from the past participle of the verb esprimere, itself derived from the Latin exprimere; x is not considered part of the standard Italian alphabet. Italians commonly refer to espresso simply as caffè (lit. 'coffee'), espresso being the ordinary coffee to order. The same happens in Portugal (café), with some regional variations (bica in Lisbon and cimbalino traditionally in Porto). In Spain, while café expreso is seen as the more formal denomination, café solo (alone, without milk) is the usual way to ask for it at an espresso bar. Some sources state that expresso is an incorrect spelling, including Garner's Modern American Usage. While the 'expresso' spelling is recognized as mainstream usage in some American dictionaries, some cooking websites call the 'x' variant illegitimate. Oxford Dictionaries online states: "The spelling expresso is not used in the original Italian and is strictly incorrect, although it is common." The Oxford English Dictionary and Merriam-Webster call it a variant spelling. The Online Etymology Dictionary calls expresso a variant of espresso. The Oxford Dictionary of American Usage and Style (2000) describes the spelling expresso as "wrong", and specifies espresso as the only correct form. The third edition of Fowler's Modern English Usage, published by the Oxford University Press in 1996, noted that the form espresso "has entirely driven out the variant expresso (which was presumably invented under the impression that it meant 'fast', 'express')".
                </p>
            <h2>History</h2>
                <h3>Precursors</h3>
                    <p>The first prototype was created in France in 1822 by Louis Bernard Rabaut. Although the result remains unknown, the experiment was proven by the drawings.
                    </p>
                    <p>In 1843, Edouard Loysel de Santais, a Frenchman, built a coffee machine capable of producing 2,000 cups of coffee per hour. He presented this machine, called a "café express", at the Paris Exposition of 1855. However, the machine did not use steam to directly force water through the coffee but rather used steam indirectly, it brewed coffee a potful at a time and the device was too complicated to have any impact on public.
                    </p>
                    <p>In 1878, German inventor Gustav Adolf Kessel patented a coffee machine called the "Revolver Coffee Machine" that featured most of the key elements of modern espresso machines.
                    </p>
                    <img src="images/espresso_machine.png" width="440" height="480" alt="An espresso machine.">
                    <p>Angelo Moriondo is often erroneously credited for inventing the beverage, since he patented a steam-driven coffee beverage making device in 1884 (No. 33/256), probably the first Italian coffee machine similar to other French and English 1800s steam-driven coffee machines. The device is "almost certainly the first Italian bar machine that controlled the supply of steam and water separately through the coffee".
                    </p>
                    <p>In 1887, the British inventors James Childs and Charles John Jones made significant improvements to espresso machine design, inventing the first portafilter and developing a system that allowed for the consecutive use of water and steam.
                    </p>
                    <p>Fourteen years later, in 1901, Italian inventor Luigi Bezzera devised and patented several improved versions of the coffee machine, the first of which was applied for on 19 December 1901. Titled "Innovations in the machinery to prepare and immediately serve coffee beverage"; Patent No. 153/94, 61707, was granted on 5 June 1902 and was the first espresso machine. In 1903, the patent was bought by Desiderio Pavoni, who founded the La Pavoni company and began to produce the machine industrially, manufacturing one machine daily in a small workshop in Via Parini, Milan.
                    </p>
                <h3>Spread</h3>
                    <p>A detailed discussion of the spread of espresso is given in (Morris 2007). In Italy, the rise of espresso consumption was associated with industrialization and urbanization, notably in Turin, Genoa, and Milan in northwest Italy. Italians also spread espresso culture into their East African colonies, Italian Somalia and Italian Eritrea. Under the Fascist regime, coffee consumed standing up was subject to price controls, encouraging the "stand at a bar" culture.
                    </p>
                    <p>In the English-speaking world, espresso became popular, particularly in the form of cappuccino, owing to the tradition of drinking coffee with milk and the exotic appeal of the foam; in the United States, this was more often in the form of lattes, with or without flavored syrups added. The latte is claimed to have been invented in the 1950s by Italian American Lino Meiorin of Caffe Mediterraneum in Berkeley, California, as a long cappuccino, and was then popularized in Seattle and then nationally and internationally by Seattle-based Starbucks in the late 1980s and 1990s.
                    </p>
                    <p>In the United Kingdom, espresso grew in popularity in the 1950s among youth, who felt more welcome in coffee shops than in pubs. Espresso was initially popular particularly within the Italian diaspora, growing in popularity as tourism to Italy exposed others to espresso, as developed by Eiscafès, established by Italians in Germany. Initially, expatriate Italian espresso bars were seen as downmarket venues, serving the working-class Italian diaspora and thus providing appeal to the alternative subculture; this can still be seen in the United States in Italian American neighborhoods, such as Boston's North End, New York's Little Italy, and San Francisco's North Beach. As specialty coffee developed in the 1980s (following earlier developments in the 1970s and even 1960s), an indigenous artisanal coffee culture developed, with espresso instead positioned as an upmarket drink.
                    </p>
                    <p>The third-wave coffee movement encompasses espresso machines as a broader coffee culture that values relationships with growers and importers and the craftsmanship involved in making specific coffee-based drinks. The mention of brands such as Synesso, La Marzocco, and Slayer, known for their top-notch equipment, highlights the differences between traditional espresso machines and super-automatic machines to achieve a complete coffee preparation.
                    </p>
                    <p>The significance of espresso machines in coffee culture today lies in their central role in the preparation of espresso-based drinks. Espresso machines are not merely tools for brewing coffee; they are symbols of craftsmanship, quality, and dedication to coffee making.
                    </p>
            <h2>Process</h2>
            <p>Espresso is made by forcing very hot water under high pressure through finely ground compacted coffee. There is no universal standard defining the process of extracting espresso, but several published definitions attempt to constrain the amount and type of ground coffee used, the temperature and pressure of the water, and the rate of extraction. Generally, one uses an espresso machine to make espresso.
            </p>
            <p>The act of producing a shot of espresso is often called "pulling" a shot, originating from lever espresso machines, with which a barista pulls down a handle attached to a spring-loaded piston, which forces hot water through the coffee at high pressure. However, it is more common for an electric pump to generate the pressure.
            </p>
            <p>Tamping down the coffee promotes the water's even penetration through the grounds. This process produces a thicker beverage by extracting both solid and dissolved components.
            </p>
