            <h2>Streaming records</h2>
            <p>In October 2015, "Thinking Out Loud" by Ed Sheeran became the first song to pass 500 million streams. A month later, Spotify announced that "Lean On" by Major Lazer and DJ Snake featuring MØ was its most-streamed song of all time with over 525 million streams worldwide. In April 2016, Rihanna overtook Justin Bieber to become the biggest artist on Spotify, with 31.3 million monthly active listeners. In May 2016, Rihanna was overtaken by Drake with 31.85 million monthly listeners. In December 2016, Drake's just-under 36 million monthly listeners were overtaken by the Weeknd's 36.068 million. Later that same month, Drake's song "One Dance" became the first song to hit one billion streams on Spotify. Upon its release in August 2017, the single "Look What You Made Me Do" by Taylor Swift earned over eight million streams within 24 hours, breaking the record for the most single-day streams for a track. On 19 June 2018, XXXTentacion's hit single "Sad!" broke Swift's single-day streaming record, amassing 10.4 million streams the day after he was fatally shot in Florida.
</p>
