            <h2>Early International launches</h2>
            <p>In February 2009, Spotify opened public registration for the free service tier in the United Kingdom. Registrations surged following the release of the mobile service, leading Spotify to halt registration for the free service in September, returning the UK to an invitation-only policy.
Spotify launched in the United States in July 2011, and offered a six-month, ad-supported trial period, during which new users could listen to an unlimited amount of music for free. In January 2012, the free trial periods began to expire, limiting users to ten hours of streaming each month and five plays per song. Using PC streaming, a similar structure to the one used today allowed the listener to play songs freely, but with ads every 4–7 songs depending on listening duration. Later that same year, in March, Spotify removed all limits on the free service tier indefinitely, including mobile devices.
In April 2016, Ek and Lorentzon wrote an open letter to Swedish politicians, demanding action in three areas that they claimed hindered the company's ability to recruit top talent as Spotify grew, including access to flexible housing, better education in the programming and development fields, and stock options. Ek and Lorentzon wrote that to continue competing in a global economy, politicians needed to respond with new policies, or thousands of Spotify jobs would be moved from Sweden to the United States.</p>

            <p>AIn February 2017, Spotify announced the expansion of its United States operations in Lower Manhattan, New York City, at 4 World Trade Center, adding approximately 1,000 new jobs and retaining 832 existing positions. The company's US headquarters are in New York City's Flatiron District.
On 14 November 2018, the company announced 13 new markets in the MENA region, including the creation of a new Arabic hub and several playlists.</p>
