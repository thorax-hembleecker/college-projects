            <h3>More on User Growth</h3>
            <p>In March 2011, Spotify announced a customer base of 1 million paying subscribers across Europe, and by September 2011, the number of paying subscribers had doubled to two million. In August 2012, Time reported 15 million active users, four million being paying Spotify subscribers. User growth continued, reaching 20 million total active users, including five million paying customers globally and one million paying customers in the United States, in December 2012. By March 2013, the service had 24 million active users, six million being paying subscribers, which grew to 40 million users (including ten million paying) in May 2014, 60 million users (including 15 million paying) in December 2014, 75 million users (20 million paying) in June 2015, 30 million paying subscribers in March 2016, 40 million paying subscribers in September 2016, and 100 million total users in June 2016. In April 2020, Spotify reached 133 million premium users. In countries affected by the COVID-19 pandemic, Spotify registered a fall in users in late February, but it has seen a recovery. In March 2022, Spotify had 182 million premium subscribers. At the end of Q2 2022, Spotify reported 188 million paying subscribers and 433 million total users.</p>
